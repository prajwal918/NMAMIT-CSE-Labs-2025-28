.MODEL SMALL
.STACK 100H

.DATA
NUM DB 04H
MSG DB 'THE FACTORIAL OF 04H = $'
FACT DW 0000H

.CODE
ASSUME CS:CODE, DS:DATA

START:
    MOV AX, DATA
    MOV DS, AX

    MOV AH, 09H
    LEA DX, MSG
    INT 21H

    MOV AX, 0001H
    MOV BL, NUM
    MOV BH, 00H
    CALL FACTORIAL

    MOV FACT, AX

    MOV BL, AH
    CALL DISPHEX
    MOV BL, AL
    CALL DISPHEX

    MOV AH, 4CH
    INT 21H

FACTORIAL PROC NEAR
    CMP BL, 01H
    JE FACT_END
    PUSH BX
    DEC BL
    CALL FACTORIAL
    POP BX
    MUL BX
FACT_END:
    RET
FACTORIAL ENDP

DISPHEX PROC NEAR
    MOV DL, BL
    MOV CL, 04H
    SHR DL, CL
    CMP DL, 0AH
    JB DISP_HIGH
    ADD DL, 07H
DISP_HIGH:
    ADD DL, 30H
    MOV AH, 02H
    INT 21H

    MOV DL, BL
    AND DL, 0FH
    CMP DL, 0AH
    JB DISP_LOW
    ADD DL, 07H
DISP_LOW:
    ADD DL, 30H
    MOV AH, 02H
    INT 21H
    RET
DISPHEX ENDP

END START
