CREATE DATABASE STUDENT201;
USE STUDENT201;

CREATE TABLE STUDENT(
    REGNO VARCHAR(20),
    NAME CHAR(30),
    MAJOR CHAR(10),
    BDATE DATE,
    PRIMARY KEY(REGNO)
);

CREATE TABLE COURSE(
    COURSE INT,
    CNAME CHAR(20),
    DEPT CHAR(20),
    PRIMARY KEY(COURSE)
);

CREATE TABLE ENROLL(
    REGNO VARCHAR(20),
    COURSE INT,
    SEM INT,
    MARKS INT,
    PRIMARY KEY(REGNO, COURSE, SEM),
    FOREIGN KEY(REGNO) REFERENCES STUDENT(REGNO)
      ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY(COURSE) REFERENCES COURSE(COURSE)
      ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE TEXT(
    BOOK_ISBN INT,
    BOOK_TITLE VARCHAR(20),
    PUBLISHER CHAR(20),
    AUTHOR CHAR(20),
    PRIMARY KEY(BOOK_ISBN)
);

CREATE TABLE BOOKADOPTION(
    COURSE INT,
    SEM INT,
    BOOK_ISBN INT,
    PRIMARY KEY(COURSE, SEM, BOOK_ISBN),
    FOREIGN KEY(COURSE) REFERENCES COURSE(COURSE)
      ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY(BOOK_ISBN) REFERENCES TEXT(BOOK_ISBN)
      ON DELETE CASCADE ON UPDATE CASCADE
);

