SELECT A.BOOK_ISBN, A.BOOK_TITLE, B.COURSE, B.CNAME
FROM TEXT A, COURSE B, BOOKADOPTION C
WHERE A.BOOK_ISBN = C.BOOK_ISBN
  AND B.COURSE = C.COURSE
  AND B.DEPT = 'CSE'
  AND B.COURSE IN (
      SELECT COURSE FROM BOOKADOPTION
      GROUP BY COURSE HAVING COUNT(*) > 2
  )
ORDER BY A.BOOK_TITLE;

-- (ii)
SELECT DISTINCT(C.DEPT) FROM COURSE C
WHERE NOT EXISTS (
    SELECT BOOK_ISBN FROM BOOKADOPTION
    WHERE COURSE IN (
        SELECT COURSE FROM COURSE WHERE DEPT = C.DEPT
    )
    AND BOOK_ISBN NOT IN (
        SELECT BOOK_ISBN FROM TEXT T
        WHERE PUBLISHER = 'MCGRAW'
    )
);

-- (iii)
SELECT T.BOOK_ISBN, T.BOOK_TITLE
FROM TEXT T, COURSE C, BOOKADOPTION B
WHERE B.COURSE = C.COURSE
  AND T.BOOK_ISBN = B.BOOK_ISBN
  AND C.DEPT IN (
      SELECT C.DEPT
      FROM COURSE C, ENROLL E
      WHERE C.COURSE = E.COURSE
      GROUP BY C.DEPT
      HAVING COUNT(DISTINCT E.REGNO) >= ALL (
          SELECT COUNT(DISTINCT E.REGNO)
          FROM COURSE C, ENROLL E
          WHERE C.COURSE = E.COURSE
          GROUP BY C.DEPT
      )
  );
